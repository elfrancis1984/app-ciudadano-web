package ec.gob.mdt.ciudadano.model.legacy;

import java.io.Serializable;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

/**
 * The persistent class for the sgi_empresa database table.
 * 
 */
@Entity
@Table(name = "sgi_empresa")
public class EntidadEmpresaSgi implements Serializable {

	private static final long serialVersionUID = 78400398160153782L;

	@Id
	@Column(name = "emp_ruc")
	private String empRuc;

	@Column(name = "aud_estado")
	private boolean audEstado;

	@Column(name = "emp_razon_social")
	private String razonSocial;

	@Column(name = "emp_direccion")
	private String direccion;

	@Column(name = "emp_mail")
	private String mail;

	@Column(name = "emp_telefono")
	private String telefono;

	@Column(name = "emp_reglamento")
	private boolean reglamento;

	@Column(name = "emp_comite")
	private boolean comite;

	@Column(name = "emp_num_sucursales")
	private int numSucursales;

	@Column(name = "emp_anio_fiscal")
	private String empAnioFiscal;

	@Column(name = "emp_utilidad_ejercicio_fiscal")
	private double empUtilidadEjercicioFiscal;

	public String getEmpRuc() {
		return this.empRuc;
	}

	public void setEmpRuc(String empRuc) {
		this.empRuc = empRuc;
	}

	public String getRazonSocial() {
		return this.razonSocial;
	}

	public void setRazonSocial(String razonSocial) {
		this.razonSocial = razonSocial;
	}

	public String getDireccion() {
		return direccion;
	}

	public void setDireccion(String direccion) {
		this.direccion = direccion;
	}

	public String getMail() {
		return mail;
	}

	public void setMail(String mail) {
		this.mail = mail;
	}

	public String getTelefono() {
		return telefono;
	}

	public void setTelefono(String telefono) {
		this.telefono = telefono;
	}

	public boolean isReglamento() {
		return reglamento;
	}

	public void setReglamento(boolean reglamento) {
		this.reglamento = reglamento;
	}

	public boolean isComite() {
		return comite;
	}

	public void setComite(boolean comite) {
		this.comite = comite;
	}

	public int getNumSucursales() {
		return numSucursales;
	}

	public void setNumSucursales(int numSucursales) {
		this.numSucursales = numSucursales;
	}

	public boolean isAudEstado() {
		return audEstado;
	}

	public void setAudEstado(boolean audEstado) {
		this.audEstado = audEstado;
	}

	public String getEmpAnioFiscal() {
		return empAnioFiscal;
	}

	public void setEmpAnioFiscal(String empAnioFiscal) {
		this.empAnioFiscal = empAnioFiscal;
	}

	public double getEmpUtilidadEjercicioFiscal() {
		return empUtilidadEjercicioFiscal;
	}

	public void setEmpUtilidadEjercicioFiscal(double empUtilidadEjercicioFiscal) {
		this.empUtilidadEjercicioFiscal = empUtilidadEjercicioFiscal;
	}
}